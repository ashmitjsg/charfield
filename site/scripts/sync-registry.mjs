// Copies the root registry/ into site/registry (gitignored) and generates a
// field-map.ts so the docs gallery renders the real published source.
import { cpSync, readdirSync, writeFileSync, rmSync, existsSync } from "node:fs";
import { resolve } from "node:path";

const src = resolve("..", "registry");
const dest = resolve("registry");

if (!existsSync(src)) {
  console.error("registry source not found:", src);
  process.exit(1);
}
if (existsSync(dest)) rmSync(dest, { recursive: true, force: true });
cpSync(src, dest, { recursive: true });

const fields = readdirSync(resolve(dest, "fields"))
  .filter((f) => f.endsWith(".ts"))
  .map((f) => f.replace(/\.ts$/, ""));

const imports = fields.map((n) => `import { ${n} } from "./fields/${n}";`).join("\n");
const entries = fields.map((n) => `  ${n},`).join("\n");

writeFileSync(
  resolve(dest, "field-map.ts"),
  `// AUTO-GENERATED by scripts/sync-registry.mjs - do not edit.
import type { FieldFn } from "./types";
${imports}

export const FIELD_MAP: Record<string, FieldFn> = {
${entries}
};

export const FIELD_NAMES = Object.keys(FIELD_MAP);
`
);

console.log(`synced registry + field-map (${fields.length} fields)`);
